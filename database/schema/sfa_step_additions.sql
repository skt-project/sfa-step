  sync_table_name      STRING    NOT NULL OPTIONS(description="Name matching the WHERE clause in sp_refresh_* procedures, e.g. 'fact_management_target', 'fact_visit_sadata'"),
  last_watermark_value STRING             OPTIONS(description="Last processed watermark as a string — cast at read time (DATE for date-based syncs)"),
