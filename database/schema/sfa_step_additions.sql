  last_run_status      STRING             OPTIONS(description="'success' | 'error'"),
  error_message        STRING             OPTIONS(description="Populated only when last_run_status = 'error'"),
  PRIMARY KEY (sync_table_name) NOT ENFORCED
)
OPTIONS (description = "Incremental sync watermarks for sp_refresh_* procedures. One row per synced table.");

